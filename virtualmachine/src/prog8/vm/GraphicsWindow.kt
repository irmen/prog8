    fun start() {
        val refreshRate = optimalRefreshRate(this)
        repaintTimer = Timer(1000/refreshRate) {
            repaint()
        }
        repaintTimer.initialDelay = 0
        repaintTimer.start()
    }

    override fun close() {
        repaintTimer.stop()
        dispose()
    }

